    public void update(int s, int minute,int m){
        
        if(firstTime)
        {
            s = 0;
            minute = 0;
            m = 0;
            firstTime = false;
        }

        String sec = displayTime(s);
        String min = displayTime(minute);
        String mil = displayTime(m);       

        System.out.println(min+":"+sec+":"+mil);
        lblDisplay.setText(min+":"+sec+":"+mil);
        lapped_time = min+":"+sec+":"+mil;

    }
   
    public String displayTime(int time)
    {
        String display;
        if(time == 00)
        {
            display = "00";
        }
        else
        {
            if(time<=10)
            {
                display = "0"+time;
            }
            else
            {
                display = Integer.toString(time);
            }
        }
        
        return display;
    }
    
